const mongoose = require("mongoose");
const { Schema } = mongoose;

const TeamSchema = new Schema({
  teamcode: {
    type: String,
    required: true,
    unique: true,
  },
  title: {
    type: String,
    required: true,
  },
  description: {
    type: String,
  },
  date: {
    type: Date,
    default: Date.now,
  },
});

module.exports = mongoose.model("team", TeamSchema);
